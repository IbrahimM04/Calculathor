#include "Calculator.h"

Calculator::Calculator()
{
}

Calculator::~Calculator() {
}

float Calculator::Add(float a, float b) {
	return a + b;
}

float Calculator::Subtract(float a, float b) {
	return a - b;
}

float Calculator::Multiply(float a, float b) {
	return a * b;
}

float Calculator::Divide(float a, float b) {
	return a / b;
}
